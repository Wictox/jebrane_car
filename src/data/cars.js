// Helper to add some random unavailable dates for demonstration
const addRandomUnavailableDates = () => {
    const today = new Date();
    cars.forEach(car => {
        if (Math.random() > 0.5) { // 50% chance to have bookings
            const start = new Date(today);
            start.setDate(today.getDate() + Math.floor(Math.random() * 10));
            const end = new Date(start);
            end.setDate(start.getDate() + Math.floor(Math.random() * 5) + 1);

            car.unavailableDates = [
                {
                    start: start.toISOString().split('T')[0],
                    end: end.toISOString().split('T')[0]
                }
            ];
        } else {
            car.unavailableDates = [];
        }
    });
};

addRandomUnavailableDates();
